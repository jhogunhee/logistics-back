-- =====================================================================
-- 2. 입고 IB_* (SCHEDULED → RECEIVING → RECEIVED → COMPLETED / CANCELLED)
-- =====================================================================

-- 입고예정(ASN) 헤더. 상태는 워크플로 단계만 표현한다 (부분입고 상태 없음).
CREATE TABLE ib_order (
    ib_order_id NUMBER(19)      GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ib_no       VARCHAR2(30)    NOT NULL,
    status      VARCHAR2(15)    NOT NULL,
    vndr_nm     VARCHAR2(100)   NOT NULL,
    expct_dt    DATE            NOT NULL,
    closed_at   TIMESTAMP,
    created_at  TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    created_by  VARCHAR2(30)    DEFAULT 'admin' NOT NULL,
    updated_at  TIMESTAMP,
    updated_by  VARCHAR2(30),
    CONSTRAINT uq_ib_no UNIQUE (ib_no),
    CONSTRAINT ck_ib_order_status CHECK (status IN ('SCHEDULED', 'RECEIVING', 'RECEIVED', 'COMPLETED', 'CANCELLED'))
);

COMMENT ON TABLE  ib_order IS '입고예정(ASN) 헤더. 부분입고 여부는 상태가 아니라 라인 수량(expct vs rcvd)에서 파생';
COMMENT ON COLUMN ib_order.ib_no     IS '입고 번호 (업무 식별자, 예: IB-20260714-001)';
COMMENT ON COLUMN ib_order.status    IS 'SCHEDULED 예정 / RECEIVING 입고중 / RECEIVED 마감 / COMPLETED 적치완료 / CANCELLED 취소(검수 시작 전만)';
COMMENT ON COLUMN ib_order.vndr_nm   IS '납품 벤더명 (v1은 벤더 마스터 없이 텍스트 보관)';
COMMENT ON COLUMN ib_order.expct_dt  IS '입고 예정일';
COMMENT ON COLUMN ib_order.closed_at IS '입고 마감(close) 시각. 마감은 미입고 잔량을 확정하는 명시적 액션';

-- 입고 라인. 부분입고/검수/적치 진행률을 수량으로만 표현한다.
CREATE TABLE ib_line (
    ib_line_id  NUMBER(19)      GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ib_order_id NUMBER(19)      NOT NULL,
    sku_id      NUMBER(19)      NOT NULL,
    expct_qty   NUMBER(12)      NOT NULL,
    rcvd_qty    NUMBER(12)      DEFAULT 0 NOT NULL,
    rjct_qty    NUMBER(12)      DEFAULT 0 NOT NULL,
    ptwy_qty    NUMBER(12)      DEFAULT 0 NOT NULL,
    created_at  TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    created_by  VARCHAR2(30)    DEFAULT 'admin' NOT NULL,
    updated_at  TIMESTAMP,
    updated_by  VARCHAR2(30),
    CONSTRAINT fk_ib_line_order FOREIGN KEY (ib_order_id) REFERENCES ib_order (ib_order_id),
    CONSTRAINT fk_ib_line_sku FOREIGN KEY (sku_id) REFERENCES sku (sku_id),
    CONSTRAINT ck_ib_line_qty CHECK (
        expct_qty > 0 AND rcvd_qty >= 0 AND rjct_qty >= 0
        AND ptwy_qty >= 0 AND ptwy_qty <= rcvd_qty
    )
);

COMMENT ON TABLE  ib_line IS '입고 라인. Lot은 라인이 아니라 입고 처리(재고 이력) 단위로 기록되므로 여기엔 두지 않는다';
COMMENT ON COLUMN ib_line.expct_qty IS '입고 예정 수량';
COMMENT ON COLUMN ib_line.rcvd_qty  IS '검수 합격 후 실제 입고(스테이징 입) 수량 누계';
COMMENT ON COLUMN ib_line.rjct_qty  IS '검수 불합격 수량 누계 (재고로 잡지 않음, v1은 별도 검수 문서 없이 수량으로만)';
COMMENT ON COLUMN ib_line.ptwy_qty  IS '적치 완료 수량 누계 (스테이징 → 보관 MOVE 반영분)';

CREATE INDEX ix_ib_line_order ON ib_line (ib_order_id);


-- =====================================================================
-- 3. 재고 INV*
-- =====================================================================

-- 현재고 스냅샷. 할당 시 락을 거는 지점 (비관적/낙관적 락 비교 대상).
CREATE TABLE inv (
    inv_id      NUMBER(19)      GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    sku_id      NUMBER(19)      NOT NULL,
    loc_id      NUMBER(19)      NOT NULL,
    lot_id      NUMBER(19)      NOT NULL,
    on_hand_qty NUMBER(12)      DEFAULT 0 NOT NULL,
    alloc_qty   NUMBER(12)      DEFAULT 0 NOT NULL,
    version     NUMBER(19)      DEFAULT 0 NOT NULL,
    created_at  TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    created_by  VARCHAR2(30)    DEFAULT 'admin' NOT NULL,
    updated_at  TIMESTAMP,
    updated_by  VARCHAR2(30),
    CONSTRAINT fk_inv_sku FOREIGN KEY (sku_id) REFERENCES sku (sku_id),
    CONSTRAINT fk_inv_loc FOREIGN KEY (loc_id) REFERENCES loc (loc_id),
    CONSTRAINT fk_inv_lot FOREIGN KEY (lot_id) REFERENCES lot (lot_id),
    CONSTRAINT uq_inv UNIQUE (sku_id, loc_id, lot_id),
    -- 동시성 실험의 최후 방어선: 애플리케이션 락이 실패해도 DB가 음수/과할당을 거부
    CONSTRAINT ck_inv_qty CHECK (on_hand_qty >= 0 AND alloc_qty >= 0 AND alloc_qty <= on_hand_qty)
);

COMMENT ON TABLE  inv IS '현재고 스냅샷. 키: SKU+Loc+Lot. 가용재고 = on_hand - alloc (파생값, 컬럼 아님)';
COMMENT ON COLUMN inv.on_hand_qty IS '실물 보유 수량. 물리 변동(RECEIVE/MOVE/ADJUST/PICK 등) 시에만 증감';
COMMENT ON COLUMN inv.alloc_qty   IS '할당(예약) 수량. 물리 이동이 아니므로 이력에 기록하지 않음';
COMMENT ON COLUMN inv.version     IS '낙관적 락 버전 (@Version). 비관적 락과의 비교 실험 대상';

CREATE INDEX ix_inv_sku ON inv (sku_id);
CREATE INDEX ix_inv_loc ON inv (loc_id);

-- 재고 이력 (수불 원장). append-only. 이력 합계 = 스냅샷 불변식의 근거 데이터.
CREATE TABLE inv_hist (
    inv_hist_id  NUMBER(19)     GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    tx_type      VARCHAR2(10)   NOT NULL,
    sku_id       NUMBER(19)     NOT NULL,
    loc_id       NUMBER(19)     NOT NULL,
    lot_id       NUMBER(19)     NOT NULL,
    qty          NUMBER(12)     NOT NULL,
    ref_doc_type VARCHAR2(10),
    ref_doc_no   VARCHAR2(30),
    created_at   TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
    created_by   VARCHAR2(30)   DEFAULT 'admin' NOT NULL,
    updated_at   TIMESTAMP,
    updated_by   VARCHAR2(30),
    CONSTRAINT fk_invh_sku FOREIGN KEY (sku_id) REFERENCES sku (sku_id),
    CONSTRAINT fk_invh_loc FOREIGN KEY (loc_id) REFERENCES loc (loc_id),
    CONSTRAINT fk_invh_lot FOREIGN KEY (lot_id) REFERENCES lot (lot_id),
    CONSTRAINT ck_invh_tx_type CHECK (tx_type IN ('RECEIVE', 'MOVE', 'ADJUST', 'PICK', 'SHIP')),
    CONSTRAINT ck_invh_qty CHECK (qty <> 0)
);

COMMENT ON TABLE  inv_hist IS '재고 수불 이력 (append-only 원장). 모든 물리 변동을 ±수량으로 기록. 스냅샷과 한 트랜잭션에서 갱신. MOVE는 출발지(-)/도착지(+) 2건으로 기록';
COMMENT ON COLUMN inv_hist.tx_type      IS 'RECEIVE 입고 / MOVE 이동(적치 포함) / ADJUST 조정 / PICK 피킹 / SHIP 출고확정';
COMMENT ON COLUMN inv_hist.qty          IS '변동 수량 (증가 +, 감소 -). 0 금지';
COMMENT ON COLUMN inv_hist.ref_doc_type IS '참조 문서 유형 (INBOUND / OUTBOUND / 수동조정 시 NULL)';
COMMENT ON COLUMN inv_hist.ref_doc_no   IS '참조 문서 번호 (입고번호/출고번호). 이력 → 원인 문서 추적용';

CREATE INDEX ix_invh_sku_created ON inv_hist (sku_id, created_at);
CREATE INDEX ix_invh_ref_doc ON inv_hist (ref_doc_no);
-- 대사(reconciliation) 배치: 키별 SUM(qty) vs inv.on_hand_qty 비교용
CREATE INDEX ix_invh_inv_key ON inv_hist (sku_id, loc_id, lot_id);


-- =====================================================================
-- 4. 출고 OUTB_* (CREATED → ALLOCATED → PICKING → PICKED → SHIPPED / CANCELLED)
-- =====================================================================

-- 출고 주문 헤더. 부분할당 여부는 상태가 아니라 라인/할당 수량에서 파생.
CREATE TABLE outb_order (
    outb_order_id NUMBER(19)    GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    outb_no     VARCHAR2(30)    NOT NULL,
    status      VARCHAR2(15)    NOT NULL,
    store_id    NUMBER(19)      NOT NULL,
    order_dt    DATE            NOT NULL,
    shipped_at  TIMESTAMP,
    created_at  TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    created_by  VARCHAR2(30)    DEFAULT 'admin' NOT NULL,
    updated_at  TIMESTAMP,
    updated_by  VARCHAR2(30),
    CONSTRAINT uq_outb_no UNIQUE (outb_no),
    CONSTRAINT fk_outb_order_store FOREIGN KEY (store_id) REFERENCES store (store_id),
    CONSTRAINT ck_outb_order_status CHECK (status IN ('CREATED', 'ALLOCATED', 'PICKING', 'PICKED', 'SHIPPED', 'CANCELLED'))
);

COMMENT ON TABLE  outb_order IS '출고 주문 헤더 (B2B 점포 출고). 피킹 시작 이후 취소는 v1 미지원';
COMMENT ON COLUMN outb_order.outb_no  IS '출고 번호 (업무 식별자, 예: OB-20260714-001)';
COMMENT ON COLUMN outb_order.status   IS 'CREATED 생성 / ALLOCATED 할당 / PICKING 피킹중 / PICKED 피킹완료 / SHIPPED 출고확정 / CANCELLED 취소';
COMMENT ON COLUMN outb_order.store_id IS '출고처 점포. 할당 시 이 점포의 잔여수명 허용률로 Lot 필터';
COMMENT ON COLUMN outb_order.order_dt IS '주문일';

-- 출고 라인. 주문 수량만 보유하고, 할당/피킹 진행은 outb_alloc에서 집계 파생.
CREATE TABLE outb_line (
    outb_line_id  NUMBER(19)    GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    outb_order_id NUMBER(19)    NOT NULL,
    sku_id        NUMBER(19)    NOT NULL,
    order_qty     NUMBER(12)    NOT NULL,
    created_at    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    created_by    VARCHAR2(30)  DEFAULT 'admin' NOT NULL,
    updated_at    TIMESTAMP,
    updated_by    VARCHAR2(30),
    CONSTRAINT fk_outb_line_order FOREIGN KEY (outb_order_id) REFERENCES outb_order (outb_order_id),
    CONSTRAINT fk_outb_line_sku FOREIGN KEY (sku_id) REFERENCES sku (sku_id),
    CONSTRAINT ck_outb_line_qty CHECK (order_qty > 0)
);

COMMENT ON TABLE  outb_line IS '출고 주문 라인. 할당/피킹 수량은 컬럼으로 두지 않고 outb_alloc 집계로 파생 (수량-상태 불일치 원천 차단)';
COMMENT ON COLUMN outb_line.order_qty IS '주문 수량';

CREATE INDEX ix_outb_line_order ON outb_line (outb_order_id);

-- 할당. 주문라인 ↔ 재고(SKU+Loc+Lot) 매핑. FEFO(+ 납품기한 필터) 결과가 기록된다.
CREATE TABLE outb_alloc (
    outb_alloc_id NUMBER(19)    GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    outb_line_id  NUMBER(19)    NOT NULL,
    inv_id        NUMBER(19)    NOT NULL,
    alloc_qty     NUMBER(12)    NOT NULL,
    picked_qty    NUMBER(12)    DEFAULT 0 NOT NULL,
    created_at    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    created_by    VARCHAR2(30)  DEFAULT 'admin' NOT NULL,
    updated_at    TIMESTAMP,
    updated_by    VARCHAR2(30),
    CONSTRAINT fk_alloc_line FOREIGN KEY (outb_line_id) REFERENCES outb_line (outb_line_id),
    CONSTRAINT fk_alloc_inv FOREIGN KEY (inv_id) REFERENCES inv (inv_id),
    CONSTRAINT ck_alloc_qty CHECK (alloc_qty > 0 AND picked_qty >= 0 AND picked_qty <= alloc_qty)
);

COMMENT ON TABLE  outb_alloc IS '재고 할당 레코드. 어떤 주문라인이 어떤 재고(SKU+Loc+Lot)를 몇 개 예약했는지. 할당 취소 시 삭제 + inv.alloc_qty 복원';
COMMENT ON COLUMN outb_alloc.alloc_qty  IS '할당 수량 (부분할당 허용: 라인 order_qty보다 합계가 작을 수 있음)';
COMMENT ON COLUMN outb_alloc.picked_qty IS '피킹 완료 수량. 피킹 리스트는 outb_alloc을 로케이션 순으로 정렬해 생성';

CREATE INDEX ix_alloc_line ON outb_alloc (outb_line_id);
CREATE INDEX ix_alloc_inv ON outb_alloc (inv_id);
